async function main() {
  console.log("\n  FreePathshala – Location Data Seeder\n");
  console.log("  ────────────────────────────────────\n");

  let totalCities = 0;
  let totalSectors = 0;
  let totalSocieties = 0;

  const actor = { uid: "system:seed" };

  for (const [city, sectors] of Object.entries(NCR_DATA)) {
    totalCities++;
    console.log(`  📍 ${city}`);

    for (const [sector, societies] of Object.entries(sectors)) {
      totalSectors++;

      if (societies.length === 0) {
        await locationService.upsertLocationHierarchy({ city, sector, society: "" }, actor);
      } else {
        for (const society of societies) {
          totalSocieties++;
          await locationService.upsertLocationHierarchy({ city, sector, society }, actor);
        }
      }
    }
  }

  console.log(`\n  ✔ Seeded successfully!`);
  console.log(`    Cities:    ${totalCities}`);
  console.log(`    Sectors:   ${totalSectors}`);
  console.log(`    Societies: ${totalSocieties}\n`);
  process.exit(0);
}

main().catch((err) => {
  console.error("\n  ✖ Seeding failed:\n", err);
  process.exit(1);
});
